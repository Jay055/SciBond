// Create a user model 
const mongoose = require('mongoose');


// Structure of the user 
const UserSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true,
    unique: true
  },
  password: {
    type: String,
    required: true
  },
  avatar: {
    type: String
  },
  date: {
    type: Date,
    default: Date.now
  }
});

// export User (modal name, Schema)
module.exports = User = mongoose.model('user', UserSchema);